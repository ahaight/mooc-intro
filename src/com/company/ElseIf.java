public class ElseIf {

    public static void main(String[] args) {
        int number = 3;

        if (number == 1) {
            System.out.println("The number is one.");
        } else if (number == 2) {
            System.out.println("The number is two.");
        } else if (number == 3) {
            System.out.println("The number is three!");
        } else {
            System.out.println("Quite a lot!");
        }
    }
}
